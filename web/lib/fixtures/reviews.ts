export function reviewsFor(subjectType: "venue" | "vendor", subjectId: string): Review[] {
  return reviews.filter(
    (r) => r.subjectType === subjectType && r.subjectId === subjectId && r.status === "published",
  );
}

export function avgRating(rs: Review[]): number {
  if (!rs.length) return 0;
  return Math.round((rs.reduce((s, r) => s + r.rating, 0) / rs.length) * 10) / 10;
}
